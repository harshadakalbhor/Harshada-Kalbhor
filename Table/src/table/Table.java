package table;

public class Table {

	public static void main(String[] args) {
		
		int a=2;
		
		int b=1;
		
		//To print the Table using "for loop"
		
		for(int i=1;i<=10;i++) {
			System.out.println(a*i);
		}
		
		
		//Print Natural Numbers using "do while  loop"
		do {
			System.out.println(b++);
		}
		while(b<=10);
		
		
		

	}

}
